package op;

public class Item {

	ItemType ItemType;
	int value;

	public ItemType getItemType() {
		return ItemType;
	}

	public void setItemType(ItemType itemType) {
		ItemType = itemType;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}

}
